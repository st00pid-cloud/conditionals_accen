package com.school.conditionals;

public class IfElseExample {
    public static void main(String[]args){
        int number = 45;

        if (number%2 == 0){
            System.out.println(number + " is even.");
        }

        if (number%2 == 1){
            System.out.println(number + " is odd");
        }
    }
}
